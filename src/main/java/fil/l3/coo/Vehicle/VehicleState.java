package fil.l3.coo.Vehicle;

/**
* The enumeration represents the possible states of a vehicle.
*
* RENTAL : Represents the state of a vehicle when it is currently rented.
* BROKEN : Represents the state of a vehicle when it is in need of repair.
* STOLEN : Represents the state of a vehicle when it has been stolen.
* AVAILABLE : Represents the state of a vehicle when it is available for use.
*/
public enum VehicleState {
    RENTAL, BROKEN, STOLEN, AVAILABLE;
}
